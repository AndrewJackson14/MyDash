#!/usr/bin/env node
/**
 * generate-legacy-redirect-map.mjs
 * ────────────────────────────────
 * Emits an nginx `map` file keyed on $uri that redirects every
 * populated stories.legacy_url to its bare-slug canonical form on
 * StellarPress.
 *
 * The SPA accepts both /<cat>/<slug> and /<slug> (verified against two
 * live URLs), so we pick the bare /<slug> as canonical and 301 the
 * category-prefixed legacy URL to it. Two map entries per story (with
 * and without trailing slash) so either shape triggers the redirect
 * without regex.
 *
 * Usage:
 *   node --env-file=.env scripts/generate-legacy-redirect-map.mjs \
 *     --host=pasoroblespress.com > legacy-redirects.map
 *
 *   # then deploy the file + an include to the nginx vhost (see
 *   # deploy note at the bottom of this file).
 *
 * Env:
 *   VITE_SUPABASE_URL | SUPABASE_URL  — required
 *   SUPABASE_SERVICE_ROLE_KEY         — required
 *
 * Flags:
 *   --host=<domain>   required — only legacy_urls whose hostname equals
 *                     this value are included. Keeps per-site maps clean.
 */

import { createClient } from '@supabase/supabase-js';

const HOST = (process.argv.find(a => a.startsWith('--host=')) || '').split('=')[1];
if (!HOST) {
  console.error('--host=<domain> required');
  process.exit(1);
}

const SUPABASE_URL = process.env.SUPABASE_URL || process.env.VITE_SUPABASE_URL;
const SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;
if (!SUPABASE_URL || !SERVICE_KEY) {
  console.error('Missing SUPABASE_URL (or VITE_SUPABASE_URL) and/or SUPABASE_SERVICE_ROLE_KEY.');
  process.exit(1);
}
const sb = createClient(SUPABASE_URL, SERVICE_KEY, { auth: { persistSession: false } });

async function loadRedirects() {
  const out = [];
  let from = 0;
  const pageSize = 1000;
  for (;;) {
    const { data, error } = await sb
      .from('stories')
      .select('id,slug,legacy_url')
      .not('legacy_url', 'is', null)
      .not('slug', 'is', null)
      .range(from, from + pageSize - 1)
      .order('id');
    if (error) throw error;
    if (!data || data.length === 0) break;
    out.push(...data);
    if (data.length < pageSize) break;
    from += pageSize;
  }
  return out;
}

function parseLegacyPath(urlStr, expectedHost) {
  try {
    const u = new URL(urlStr);
    if (u.hostname !== expectedHost) return null;
    // Normalize to a path with trailing slash removed. We'll emit both
    // shapes as map keys so we don't need regex.
    return u.pathname.replace(/\/+$/, '');
  } catch { return null; }
}

async function main() {
  const rows = await loadRedirects();
  console.error(`Loaded ${rows.length} stories with legacy_url set`);

  const seen = new Set();
  const entries = [];
  let skipped = 0;
  for (const r of rows) {
    const path = parseLegacyPath(r.legacy_url, HOST);
    if (!path || path === `/${r.slug}`) { skipped++; continue; }
    const target = `/${r.slug}`;
    // Skip any dup legacy paths (shouldn't exist thanks to the
    // partial unique index, but be safe).
    if (seen.has(path)) continue;
    seen.add(path);
    entries.push({ path, target });
  }
  console.error(`  ${entries.length} redirect entries, ${skipped} skipped (not on host or already bare-slug)`);

  // Emit the nginx map. Use `$uri` (path-only after normalization) so
  // query strings don't break matching; the 301 target is path-only
  // which nginx resolves as same-host by default.
  process.stdout.write(`# Generated by scripts/generate-legacy-redirect-map.mjs\n`);
  process.stdout.write(`# ${entries.length} legacy path → bare-slug redirects for ${HOST}\n`);
  process.stdout.write(`# Do not hand-edit. Regenerate after audit --apply runs.\n\n`);
  process.stdout.write(`map $uri $legacy_redirect {\n`);
  process.stdout.write(`    default "";\n`);
  for (const e of entries) {
    // Emit both with and without trailing slash.
    process.stdout.write(`    "${e.path}"  "${e.target}";\n`);
    process.stdout.write(`    "${e.path}/" "${e.target}";\n`);
  }
  process.stdout.write(`}\n`);

  console.error(`\nDeploy:`);
  console.error(`  1. scp this file to the host at /etc/nginx-rc/conf.d/<vhost>.legacy-redirects.conf`);
  console.error(`  2. include the map from http {} (see deploy note in-source)`);
  console.error(`  3. add the 301 trigger to the vhost main-before location block`);
  console.error(`  4. nginx-rc -t && systemctl reload nginx-rc`);
}

main().catch(e => { console.error(e); process.exit(1); });

/* Deploy note
 * ────────────
 * The `map` directive is http-level, so the generated file has to be
 * pulled in from an http-context include — not a server include. For
 * RunCloud this means adding the map + trigger through /etc/nginx-rc/conf.d
 * at the top level (outside any server {}), OR wrapping the redirect
 * trigger in the vhost's main-before hook and loading the map from the
 * server's outer http context via a sibling include file.
 *
 * Simplest working layout on the stellarpress host:
 *
 *   /etc/nginx-rc/conf.d/legacy-redirects-map.pbp.conf   ← this file's output
 *   /etc/nginx-rc/extra.d/pasoroblespressSP.location.main-before.legacy-redirect.conf:
 *     if ($legacy_redirect != "") { return 301 https://$host$legacy_redirect; }
 *
 * `conf.d/*.conf` is already globbed into the http block by RunCloud's
 * nginx-rc.conf, so the map loads without server context. The `if` is
 * acceptable here because all it does is a return — no rewrites/access
 * phase surprises.
 */
