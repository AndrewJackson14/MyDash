#!/usr/bin/env node
/**
 * generate-legacy-redirect-map.mjs
 * в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
 * Emits a single nginx `map` keyed on "$host$uri" that redirects every
 * populated stories.legacy_url to its bare-slug canonical form on
 * StellarPress. One file covers every legacy domain вЂ” keys are
 * prefixed with the host so PRP and ANews entries can't collide on a
 * shared path, and both vhosts' main-before hooks share the same
 * `$legacy_redirect` variable without duplicate-map conflicts.
 *
 * StellarPress's SPA accepts both /<cat>/<slug> and /<slug> (verified
 * live), so we pick the bare /<slug> as canonical and 301 the
 * category-prefixed legacy URL to it. Two map entries per story (with
 * and without trailing slash) so either shape triggers the redirect
 * without regex.
 *
 * Usage:
 *   node --env-file=.env scripts/generate-legacy-redirect-map.mjs \
 *     > legacy-redirects.map
 *
 *   # then deploy the file + add a trigger include on each vhost
 *   # (see deploy note at the bottom of this file).
 *
 * Env:
 *   VITE_SUPABASE_URL | SUPABASE_URL  вЂ” required
 *   SUPABASE_SERVICE_ROLE_KEY         вЂ” required
 *
 * Flags:
 *   --host=<domain>   optional вЂ” restrict output to a single domain.
 *                     Keys still include the host prefix for consistency
 *                     with the combined map. Intended for debugging.
 */

import { createClient } from '@supabase/supabase-js';

const HOST_FILTER = (process.argv.find(a => a.startsWith('--host=')) || '').split('=')[1] || null;

const SUPABASE_URL = process.env.SUPABASE_URL || process.env.VITE_SUPABASE_URL;
const SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;
if (!SUPABASE_URL || !SERVICE_KEY) {
  console.error('Missing SUPABASE_URL (or VITE_SUPABASE_URL) and/or SUPABASE_SERVICE_ROLE_KEY.');
  process.exit(1);
}
const sb = createClient(SUPABASE_URL, SERVICE_KEY, { auth: { persistSession: false } });

async function loadRedirects() {
  const out = [];
  let from = 0;
  const pageSize = 1000;
  for (;;) {
    const { data, error } = await sb
      .from('stories')
      .select('id,slug,legacy_url')
      .not('legacy_url', 'is', null)
      .not('slug', 'is', null)
      .range(from, from + pageSize - 1)
      .order('id');
    if (error) throw error;
    if (!data || data.length === 0) break;
    out.push(...data);
    if (data.length < pageSize) break;
    from += pageSize;
  }
  return out;
}

function parseLegacy(urlStr) {
  try {
    const u = new URL(urlStr);
    // Normalize to a path with trailing slash removed. We'll emit both
    // shapes as map keys so we don't need regex.
    return { host: u.hostname, path: u.pathname.replace(/\/+$/, '') };
  } catch { return null; }
}

async function main() {
  const rows = await loadRedirects();
  console.error(`Loaded ${rows.length} stories with legacy_url set`);

  const seen = new Set();
  const entries = [];
  const hostTally = {};
  let skipped = 0;
  for (const r of rows) {
    const parsed = parseLegacy(r.legacy_url);
    if (!parsed) { skipped++; continue; }
    if (HOST_FILTER && parsed.host !== HOST_FILTER) { skipped++; continue; }
    if (parsed.path === `/${r.slug}`) { skipped++; continue; }  // already bare
    const key = `${parsed.host}${parsed.path}`;  // host-prefixed key, no scheme
    if (seen.has(key)) continue;
    seen.add(key);
    entries.push({ host: parsed.host, path: parsed.path, target: `/${r.slug}` });
    hostTally[parsed.host] = (hostTally[parsed.host] || 0) + 1;
  }
  console.error(`  ${entries.length} redirect entries, ${skipped} skipped`);
  for (const [h, n] of Object.entries(hostTally)) console.error(`    ${h}: ${n}`);

  // Emit the nginx map keyed on `$host$uri`. One map covers every
  // legacy domain вЂ” the host prefix prevents collisions between sites
  // that share a path shape. Trigger in each vhost stays identical:
  //   if ($legacy_redirect != "") { return 301 https://$host$legacy_redirect$is_args$args; }
  process.stdout.write(`# Generated by scripts/generate-legacy-redirect-map.mjs\n`);
  process.stdout.write(`# ${entries.length} legacy-URL в†’ bare-slug redirects across ${Object.keys(hostTally).length} host(s)\n`);
  process.stdout.write(`# Do not hand-edit. Regenerate after audit --apply runs.\n\n`);
  process.stdout.write(`map $host$uri $legacy_redirect {\n`);
  process.stdout.write(`    default "";\n`);
  for (const e of entries) {
    // Emit both with and without trailing slash.
    process.stdout.write(`    "${e.host}${e.path}"  "${e.target}";\n`);
    process.stdout.write(`    "${e.host}${e.path}/" "${e.target}";\n`);
  }
  process.stdout.write(`}\n`);

  console.error(`\nDeploy:`);
  console.error(`  1. scp to /etc/nginx-rc/conf.d/legacy-redirects-map.conf (http-level, covers every vhost)`);
  console.error(`  2. on each legacy-site vhost, drop a main-before include with:`);
  console.error(`       if ($legacy_redirect != "") { return 301 https://$host$legacy_redirect$is_args$args; }`);
  console.error(`  3. nginx-rc -t && systemctl reload nginx-rc`);
}

main().catch(e => { console.error(e); process.exit(1); });

/* Deploy note
 * в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
 * The `map` directive is http-level, so the generated file has to be
 * pulled in from an http-context include вЂ” not a server include. For
 * RunCloud this means adding the map + trigger through /etc/nginx-rc/conf.d
 * at the top level (outside any server {}), OR wrapping the redirect
 * trigger in the vhost's main-before hook and loading the map from the
 * server's outer http context via a sibling include file.
 *
 * Simplest working layout on the stellarpress host:
 *
 *   /etc/nginx-rc/conf.d/legacy-redirects-map.pbp.conf   в†ђ this file's output
 *   /etc/nginx-rc/extra.d/pasoroblespressSP.location.main-before.legacy-redirect.conf:
 *     if ($legacy_redirect != "") { return 301 https://$host$legacy_redirect; }
 *
 * `conf.d/*.conf` is already globbed into the http block by RunCloud's
 * nginx-rc.conf, so the map loads without server context. The `if` is
 * acceptable here because all it does is a return вЂ” no rewrites/access
 * phase surprises.
 */
